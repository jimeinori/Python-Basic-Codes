#==================={Conditions/ControlFlow}=====================#

#Control flow - gives us this ability to choose among outcomes.
#comparators - used to compare things.

#Comparator P1 (==) and (!=)
num1 = 100
num2 = 200

if num1 == num2: 
	print(True)
elif num1 != num2:
	print(False)

#Comparator P2 (<) and (>)
num3 = 35
num4 = 20

if num3 > num4:
	print(True)
elif num3 < num4:
	print(False)

#Comparator P3 (<=) and (>=)
num5= 20
num6= 50

if num5 >= num6:
	print(True)
elif num5 <= num6:
	print(False)
