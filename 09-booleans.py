'''
Sometimes we have a need for variables
that are either true or false.
This datatype, which can only ever
take one of two values, is called a boolean.
'''

a = True
b = False

'''
A boolean is actually a special case of an integer.
A value of True corresponds to an integer value of 1,
and will behave the same.
A value of False corresponds to an integer value of 0.
'''

