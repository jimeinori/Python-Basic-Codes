'''
Variables can also hold numeric values.
The simplest kind of number in Python
is the integer, which is a whole number with no decimal point.
'''

int1 = 5
int2 = 10
int3 = -15

'''
A number with a decimal point is called a float.
You can define floats with numbers after
the decimal point or by just including a decimal point at the end.
'''

float1 = 5.0
float2 = 20.
float3 = -3.5

'''
You can also define a float using
scientific notation, with e indicating the power of 10.
'''

# this evaluates to 120:
float4 = 1.2e2
